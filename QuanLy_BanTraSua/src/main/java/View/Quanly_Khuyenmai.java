    private void btADDKhuyenmaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btADDKhuyenmaiActionPerformed
        // TODO add your handling code here:
        if (txtmakhuyenmai.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Mã Khuyến mãi Not null", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (txtSotiengiam.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "số tiền giảm NOT NULL", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (txtNgayBatDau.getDate().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Ngày bắt đầu Not null", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (txtNgayKetThuc.getDate().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Ngày Kết thúc Not null", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (txtDieukienkhuyenmai.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Điều kiện NOT NULL", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {

            KhuyenMai km = new KhuyenMai();
            km.setMa(txtmakhuyenmai.getText());
            km.setSoTienGiam(Double.valueOf(txtSotiengiam.getText()));
            km.setNgayBatDau(txtNgayBatDau.getDate());
            km.setNgayketThuc(txtNgayKetThuc.getDate());
            km.setDieukien(txtDieukienkhuyenmai.getText());

            kmitf.createKhuyenmai(km);

            filltotableKhuyenMai();
            clearDataKhuyenMai();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Insert failure", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btADDKhuyenmaiActionPerformed

