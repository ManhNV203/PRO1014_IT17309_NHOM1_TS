    private void btFindKhuyenmaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btFindKhuyenmaiActionPerformed
        // TODO add your handling code here:

        List<KhuyenmaiViewModel> lst_kmvmd = new ArrayList<>();
        for (int i = 0; i < kmitf.getAllKM().size(); i++) {
            if (kmitf.getAllKM().get(i).getDieukien().equalsIgnoreCase(txtNhapmakhuyenmai.getText())) {
                lst_kmvmd.add(kmitf.getAllKM().get(i));
            }
        }
        DefaultTableModel tblModel = (DefaultTableModel) tableKhuyenmai.getModel();
        tblModel.setColumnIdentifiers(new Object[]{"ID", "Mã khuyến mãi", "Số tiền giảm", "Ngày bắt đầu", "Ngày kết thúc", "Điều kiện"});
        tblModel.setRowCount(0);
        for (KhuyenmaiViewModel x : lst_kmvmd) {
            tblModel.addRow(new Object[]{x.getId(), x.getMa(), x.getSoTienGiam(), x.getNgayBatDau(), x.getNgayketThuc(), x.getDieukien()});
        }
    }//GEN-LAST:event_btFindKhuyenmaiActionPerformed

