    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        if (txtTimKiem.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "ban chua nhap ma ");
            return;
        }
        try {
            NhanVienViewModel nvVMD = new NhanVienViewModel();
            nvVMD = service.getone(txtTimKiem.getText());

            txtDiaChi.setText(nvVMD.getDiaChi());
            txtEmail.setText(nvVMD.getEmail());
            txtHoTen.setText(nvVMD.getHoTen());
            txtMaNV.setText(nvVMD.getMa());
            txtMatKhau.setText(nvVMD.getMatKhau());
            txtSDT.setText(nvVMD.getSDT());

            if (nvVMD.getGioiTinh().equalsIgnoreCase("Nam")) {
                rdbNam.setSelected(true);
            } else {
                rdbNu.setSelected(true);
            }

            dcNgaySinh.setDate(nvVMD.getNgaySinh());
            txtMaNV.setEnabled(false);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "nhan vien khong ton tai");
        }

    }//GEN-LAST:event_btnTimKiemActionPerformed

