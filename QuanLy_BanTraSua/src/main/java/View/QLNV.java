    public void fillToTB(List<NhanVienViewModel> nv) {
        model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        nv.forEach((t) -> {
            model.addRow(t.dataRow());
        });

    }

    public void fillToForm() {
        int index = tblNhanVien.getSelectedRow();
        List<NhanVienViewModel> listVMD = new ArrayList<>();
        listVMD = service.all();
        NhanVienViewModel nvVMD = new NhanVienViewModel();
        nvVMD = listVMD.get(index);
        txtDiaChi.setText(nvVMD.getDiaChi());
        txtEmail.setText(nvVMD.getEmail());
        txtHoTen.setText(nvVMD.getHoTen());
        txtMaNV.setText(nvVMD.getMa());
        txtMatKhau.setText(nvVMD.getMatKhau());
        txtSDT.setText(nvVMD.getSDT());

        if (nvVMD.getGioiTinh().equalsIgnoreCase("Nam")) {
            rdbNam.setSelected(true);
        } else {
            rdbNu.setSelected(true);
        }

        dcNgaySinh.setDate(nvVMD.getNgaySinh());
    }

    public void getListSex() {
        List<NhanVienViewModel> nvVMD = new ArrayList<>();
        if (cboLocGioiTinh.getSelectedItem().equals("Nam")) {
            nvVMD = service.getBySex("Nam");
            fillToTB(nvVMD);
        } else if (cboLocGioiTinh.getSelectedItem().equals("Nu")) {
            nvVMD = service.getBySex("Nu");
            fillToTB(nvVMD);
        } else {

            listNV = service.all();
            fillToTB(listNV);
        }

    }

